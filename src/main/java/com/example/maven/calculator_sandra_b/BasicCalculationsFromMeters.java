public interface BasicCalculationsFromMeters {
	/**
	 * method to count from meter to inch
	 * 
	 * @param valueMeterToInch
	 */
	double meterToInch(double valueMeterToInch);

	/**
	 * method to count meter to foot
	 * 
	 * @param valueMeterToFoot
	 */
	double meterToFoot(double valueMeterToFoot);

	/**
	 * method to count meter to mm
	 * 
	 * @param valueMeterToMm
	 */
	double meterToMm(double valueMeterToMm);

	/**
	 * method to count meter to cm
	 * 
	 * @param valueMeterToCm
	 */
	double meterToCm(double valueMeterToCm);

	/**
	 * method to count meter to mile
	 * 
	 * @param valueMeterToMile
	 */
	double meterToMile(double valueMeterToMile);

	/**
	 * method to count meter to Yard
	 * 
	 * @param valueMeterToYard
	 */
	double meterToYard(double valueMeterToYard);

	/**
	 * method to count meter to km
	 * 
	 * @param valueMeterToKm
	 */
	double meterToKm(double valueMeterToKm);
}
